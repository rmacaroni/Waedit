/* ////////////////////////////////////////////////////////////////////////////
				  selection.cs
///////////////////////////////////////////////////////////////////////////////
DESCRIPTION:	Here's where we keep track of the endpoints of the area within
                the text buffer selected by the Block and Delete commands.

REVISIONS:	 3 Aug 16 - RAC - Genesis
		 6 Aug 16 - RAc - Rewrote to be a part of the MainForm class
//////////////////////////////////////////////////////////////////////////// */

using System;

namespace waedit
{
partial class MainForm
{

/* ////////////////////////////////////////////////////////////////////////////
				     Data
//////////////////////////////////////////////////////////////////////////// */

int blockAnchor = -1;		// If a selection is under way, contains the
                                //  text buffer index of whichever endpoint was
                                //  selected first.  Otherwise (if no selection
                                //  is under way) contains -1.

/* ////////////////////////////////////////////////////////////////////////////
			     SelectionStartPoint()
			     SelectionEndPoint()
///////////////////////////////////////////////////////////////////////////////
DESCRIPTION:	These functions return the start and end points of the selected
		block.

REVISIONS:	 3 Aug 16 - RAC - Genesis
//////////////////////////////////////////////////////////////////////////// */

int SelectionStartPoint()
{
    return Math.Min(blockAnchor, currentChar);
}

int SelectionEndPoint()
{
    return Math.Max(blockAnchor, currentChar);
}

/* ////////////////////////////////////////////////////////////////////////////
			       ClearSelection()
///////////////////////////////////////////////////////////////////////////////
DESCRIPTION:	What is says.

REVISIONS:	 3 Aug 16 - RAC - Genesis
//////////////////////////////////////////////////////////////////////////// */

void ClearSelection()
{
    blockAnchor = -1;
}

/* ////////////////////////////////////////////////////////////////////////////
				  Selecting()
///////////////////////////////////////////////////////////////////////////////
DESCRIPTION:	This function returns true if a selection is under way, or
		false otherwise.

REVISIONS:	19 Aug 16 - RAC - Genesis
//////////////////////////////////////////////////////////////////////////// */

bool Selecting() { return blockAnchor != -1; }

}						// End class MainForm
}						// End namespace waedit

